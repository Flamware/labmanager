package fr.ciadlab.labmanager.utils;

import java.util.function.BiConsumer;

import com.google.gson.JsonObject;

/** Interface that represents an object that could be exported to JSON object.
 * 
 * @author $Author: sgalland$
 * @version $Name$ $Revision$ $Date$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 * @since 2.0.0
 */
public interface JsonExportable {

	/** Fill the given JSON object with the content of this publication.
	 *
	 * @param json the JSON object to fill up.
	 * @see #forEachAttribute(BiConsumer)
	 */
	void toJson(JsonObject json);

}
